#include <stdio.h>
unsigned invert(unsigned x, unsigned p, unsigned n)
{
    return x ^ (((1 << n) - 1) << p);
}

/*
Създава се маска от n единици - (1 « n) - 1 - ако n = 4, резултатът ще е 1111.
• Маската се измества на позиция p.
• XOR инвертира точно тези битове и запазва останалите - 1010 1011 1101 1001
^ 0000 1111 0000 0000 = 1010 0100 1101 1001.
*/